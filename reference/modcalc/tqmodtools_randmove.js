function clean_input(input)
{
	var attacks = parseInt(input);
	if (isNaN(attacks))
	{
		document.getElementById("errors").firstChild.nodeValue = "Please enter an integer for the number of attacks.";
	} else if (attacks < 1) {
		document.getElementById("errors").firstChild.nodeValue = "The number of attacks must be an integer greater than zero.";
	} else {
		return attacks;
	}
}

function pick_attacks(attacks, bound)
{
	var attack_list = new Array();

	for (var i = 0; i < attacks; i++)
	{
		rand = Math.floor(Math.random() * bound) + 1;
		attack_list[i] =  k[rand];
	}

	return attack_list;
}

function use_metronome()
{
	var attacks = clean_input(document.getElementById("numattacks").value);
	if (attacks)
	{
		if (document.getElementById("errors").firstChild.nodeValue)
		{
			document.getElementById("errors").firstChild.nodeValue = " ";
		}
		if (document.getElementById("truetype").checked)
		{
			var attack_list = pick_attacks(attacks, 660); //this should be the index of the last move valid for true metronome
			document.getElementById("randmoveoutput").firstChild.nodeValue = attack_list.join(", ");
		} else {
			var attack_list = pick_attacks(attacks, 710); //this should be the index of the last move overall
			document.getElementById("randmoveoutput").firstChild.nodeValue = attack_list.join(", ");
		}
	}
}
